void Bell_init()
{
    pinMode(BELL_PIN, OUTPUT);
    Bell_turn_off();
}


void Bell_turn_off()
{
    digitalWrite(BELL_PIN, HIGH);
}


void Bell_turn_on()
{
    digitalWrite(BELL_PIN, LOW);
}